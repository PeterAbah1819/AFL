  /*Simple Interest Formula = p*r*t/100*/
     
  var p, t, r, SI;
    
    // p = principal
    // t = time
    // r = rate
    // SI = simple interest
    
    // Calculate simple interest
    p = 40;
    t = 5;
    r = 10;
    
    SI = (p * t * r) / 100;
    
    // Output
    console.log("Simple Interest = " + SI);